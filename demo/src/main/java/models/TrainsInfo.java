package models;

public class TrainsInfo {
    private int totalTrains;
    private int totalPassengers;
    private int totalRailways;

    public int getTotalTrains() {
        return totalTrains;
    }
    public void setTotalTrains(int totalTrains) {
        this.totalTrains = totalTrains;
    }

    public int getTotalPassengers() {
        return totalPassengers;
    }

    public void setTotalPassengers(int totalPassengers) {
        this.totalPassengers = totalPassengers;
    }

    public int getTotalRailways() {
        return totalRailways;
    }

    public void setTotalRailways(int totalRailways) {
        this.totalRailways = totalRailways;
    }
}
