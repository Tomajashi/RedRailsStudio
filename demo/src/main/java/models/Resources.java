package models;

public class Resources {
    private int DB_coin;
    private int energy_capacity;
    private int man_power;

    // Getters and Setters
    public int getDB_coin() {
        return DB_coin;
    }

    public void setDB_coin(int DB_coin) {
        this.DB_coin = DB_coin;
    }

    public int getEnergy_capacity() {
        return energy_capacity;
    }

    public void setEnergy_capacity(int energy_capacity) {
        this.energy_capacity = energy_capacity;
    }

    public int getMan_power() {
        return man_power;
    }

    public void setMan_power(int man_power) {
        this.man_power = man_power;
    }
}